
package modelo;


public class Aprendiz {
    int id;
    int docuA;
    
    String NombreA;
    String ApellidoA;
    String CorreoA;
    int TeleA;

    public Aprendiz() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDocuA() {
        return docuA;
    }

    public void setDocuA(int docuA) {
        this.docuA = docuA;
    }

    public String getNombreA() {
        return NombreA;
    }

    public void setNombreA(String NombreA) {
        this.NombreA = NombreA;
    }

    public String getApellidoA() {
        return ApellidoA;
    }

    public void setApellidoA(String ApellidoA) {
        this.ApellidoA = ApellidoA;
    }

    public String getCorreoA() {
        return CorreoA;
    }

    public void setCorreoA(String CorreoA) {
        this.CorreoA = CorreoA;
    }

    public int getTeleA() {
        return TeleA;
    }

    public void setTeleA(int TeleA) {
        this.TeleA = TeleA;
    }
    
    
}

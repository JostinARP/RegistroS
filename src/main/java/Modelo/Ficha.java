
package Modelo;


public class Ficha {
    private int CodF;
    private int CantAprendicesF;
    private int CodProgramaF;
    private int DocaprendizF;

    public Ficha() {
    }

    public int getCodF() {
        return CodF;
    }

    public void setCodF(int CodF) {
        this.CodF = CodF;
    }

    public int getCantAprendicesF() {
        return CantAprendicesF;
    }

    public void setCantAprendicesF(int CantAprendicesF) {
        this.CantAprendicesF = CantAprendicesF;
    }

    public int getCodProgramaF() {
        return CodProgramaF;
    }

    public void setCodProgramaF(int CodProgramaF) {
        this.CodProgramaF = CodProgramaF;
    }

    public int getDocaprendizF() {
        return DocaprendizF;
    }

    public void setDocaprendizF(int DocaprendizF) {
        this.DocaprendizF = DocaprendizF;
    }
    
    
}

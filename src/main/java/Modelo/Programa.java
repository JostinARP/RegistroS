
package Modelo;


public class Programa {
    private int codP;
    private String nomP;

    public Programa() {
    }

    public int getCodP() {
        return codP;
    }

    public void setCodP(int codP) {
        this.codP = codP;
    }

    public String getNomP() {
        return nomP;
    }

    public void setNomP(String nomP) {
        this.nomP = nomP;
    }
    
    
}
